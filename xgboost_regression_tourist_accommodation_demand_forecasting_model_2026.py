def _row(label, v1, v2, v3, fmt):
    best = min([v1, v2, v3])
    def cell(v):
        style = ' style="background:#d4edda;font-weight:bold"' if v == best else ''
        return f'<td{style}>{fmt.format(v)}</td>'
    return f'<tr><td>{label}</td>{cell(v1)}{cell(v2)}{cell(v3)}</tr>'

